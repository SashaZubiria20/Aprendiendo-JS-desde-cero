// Operadores:

let a = 5;
let b = 2;

// suma
console.log(a+b);
// resta
console.log(a-b);
// multiplicacion
console.log(a*b);
// division
console.log(a/b);
// modulo
console.log(a%b);

// concatenacion
let nombre = "Juan";
let edad = 25;
let mensaje = `Hola, mi nombre es ${nombre} y tengo ${edad} años.`;
console.log(mensaje);

// incremento / decremento

let c = 10;
console.log(c);
console.log(++c);

let d = 5;
console.log(d);
console.log(--d);

// Asignacion

let e = 10;
console.log(e);

e += 3;
console.log(e);

e -= 5;
console.log(e);

e *= 5;
console.log(e);

e /= 2;
console.log(e);